/* 5.14(Prefix vs.Postfix Increment Operators) What is the difference between the prefix increment operator and the postfix increment operator?
  
Answer:

    Prefix increment operator increments value of variable by 1 and use the new value in the expression in which a resides.
    Postfix increment operator increments value of variable by 1, but uses the original value in the expression in which a resides.  */